    void mostrardatos(String valor){
        conexion con = new conexion();
           Connection cone = (Connection) con.getConnection();

        DefaultTableModel modelo1 = new DefaultTableModel();
        modelo1.addColumn("N째 Factura");
        modelo1.addColumn("cai");
        modelo1.addColumn("costo");
        modelo1.addColumn("descuento");
        modelo1.addColumn("isv");
        modelo1.addColumn("N째 Boleto");
        modelo1.addColumn("Id Cliente");
        modelo1.addColumn("RTN");
        modelo1.addColumn("Total");
        
        tablafac.setModel(modelo1);
        String sql ="";
        if (valor.equals(""))
        {
            sql= "SELECT * FROM factura";
        } else{
            sql = "SELECT * FROM facuta WHERE (num_factura ='"+valor+"' OR cai='"+valor+"' OR costo = '"+valor+"' OR descuento = '"+valor+"' OR isv = '"+valor+"' OR num_boleto ='"+valor+"' OR id_cliente ='"+valor+"' OR rtn ='"+valor+"' OR total ='"+valor+"')";
        }
        
        String []datos = new String [9];
        try{
             Statement st = cone.createStatement();
             ResultSet rs = st.executeQuery(sql);
             while(rs.next()){
                 datos[0] = rs.getString(1);
                 datos[1] = rs.getString(2);
                 datos[2] = rs.getString(3);
                 datos[3] = rs.getString(4);
                 datos[4] = rs.getString(5);
                 datos[5] = rs.getString(6);
                 datos[6] = rs.getString(7);
                 datos[7] = rs.getString(8);
                 datos[8] = rs.getString(9);
                 
                 modelo1.addRow(datos);
        }
             tablafac.setModel(modelo1);
               
        
    }           catch (SQLException ex) {
                    Logger.getLogger(frmfactura1.class.getName()).log(Level.SEVERE, null, ex);
                }
        }

