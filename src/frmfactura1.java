    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
            // TODO add your handling code here:
            PreparedStatement ps = null;
            try {
                ps = cone.prepareStatement("INSERT INTO `factura` (`num_factura`,`cai`,`costo`,`descuento`,`isv`,`num_boleto`,id_cliente`,`rtn`,`total`) VALUES (?,?,?,?,?,?,?,?,? )");
            } catch (SQLException ex) {
                Logger.getLogger(frmfactura1.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                ps.setString(1,txt_numero.getText());
                ps.setString(2,txt_cai.getText());
                ps.setString(3,txt_costo.getText());
                ps.setString(4,txt_desc.getText());
                ps.setString(5,txt_isv.getText());
                ps.setString(6,txt_boleto.getText());
                ps.setString(7,txt_id.getText());
                ps.setString(8,txt_rtn.getText());
                ps.setString(9,txt_total.getText());

                int a=  ps.executeUpdate();
                if (a>0){
                    JOptionPane.showMessageDialog(null, "Registro exitoso");
                    mostrardatos("");
                }
                else{
                    JOptionPane.showMessageDialog(null, "Error al agregar");
                }
            } catch (SQLException ex) {
                Logger.getLogger(frmfactura1.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btnguardarActionPerformed

